# # 2. Уравнения и неравенства
#
# ## 2.1. Понятие уравнения
# - Уравнение – равенство, содержащее переменную 
# - Состоит из левой части, правой части и знака равно
# - Корень уравнения – значение переменной, обращающее уравнение в верное числовое равенство
# - Решить уравнение – найти все корни или доказать их отсутствие
# - Уравнение может иметь:
#   - Один корень
#   - Несколько корней
#   - Бесконечно много корней
#   - Не иметь корней
#
# ## 2.2. Преобразование уравнений
# Разрешенные действия:
# - Раскрытие скобок
# - Приведение подобных слагаемых
# - Перенос членов из части в часть с противоположным знаком
# - Умножение/деление обеих частей на число ≠ 0
# - Замена частей местами
#
# Запрещено:
# - Сокращать на множитель с переменной (ведет к потере корней)
#
# ## 2.3. Квадратное уравнение
# - Вид: ax² + bx + c = 0, где a ≠ 0
# - Решение через дискриминант: D = b² - 4ac
# - Если D > 0: два корня x₁,₂ = (-b ± √D)/(2a)
# - Если D = 0: один корень x = -b/(2a)
# - Если D < 0: нет действительных корней
#
# ## 2.4. Неравенства 
# - Содержат знаки >, <, ≥, ≤
# - Строгие (>, <) и нестрогие (≥, ≤)
# - Решением обычно являются промежутки значений
# - Решить неравенство – найти все значения переменной, обращающие его в верное числовое неравенство
#
# ## 2.5. Действия с неравенствами
# - Те же, что с уравнениями
# - При умножении/делении на отрицательное число знак неравенства меняется на противоположный
# - При переносе из части в часть знак меняется на противоположный
#
# ## 2.6. Метод интервалов
# 1. Найти корни соответствующего уравнения и недопустимые значения
# 2. Отметить точки на числовой прямой
# 3. Определить знак функции на каждом интервале
# 4. Выбрать нужные интервалы согласно знаку неравенства
#
# ## 2.7. Уравнения и неравенства с модулем
# - |x| = a имеет два корня: x = ±a при a > 0
# - |x| < a равносильно -a < x < a
# - |x| > a равносильно x < -a или x > a
# - При возведении в квадрат модуль исчезает
#
# ## 2.8. Понятие системы
# - Система – множество условий, выполняющихся одновременно
# - Решение должно удовлетворять всем условиям
# - Может быть:
#   - Несовместной (без решений)
#   - Иметь одно решение
#   - Иметь несколько решений
#   - Иметь бесконечно много решений
#
# ## 2.9. Уравнения и неравенства с несколькими переменными
# - Могут содержать две и более переменные
# - Могут иметь:
#   - Единственное решение
#   - Бесконечно много решений
#   - Не иметь решений
# - Решаются путем выражения одной переменной через другую
